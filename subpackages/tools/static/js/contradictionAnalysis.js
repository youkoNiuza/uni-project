export const getContradictionAnalysisResult = (improve, deterioration) => {
	const des = `改善${EngineeringParameters[improve]}导致${EngineeringParameters[deterioration]}恶化`
	const res = ContradictionMatrix[improve][deterioration]
	if(res === '0') {
		return {
			type: 0,
			des: des,
			payload: '产生的矛盾是物理矛盾，不在技术矛盾应用范围之内。\n请返回重新选择矛盾参数！'
		}
	}
	
	if(res === '-1') {
		return {
			type: 0,
			des: des,
			payload: '没有找到合适的发明原理来解决问题。但这只是表示研究的局限，并不代表不能够应用发明原理。\n请返回重新选择矛盾参数！'
		}
	}
	
	if(res === '') {
		return {
			type: 0,
			des: des,
			payload: '没有找到合适的发明原理来解决问题。\n请返回重新选择矛盾参数！'
		}
	}
	
	const arrPrincipleId = res.split(',')
	const payload = []
	for(let i = 0; i < arrPrincipleId.length; i++) {
		const principleId = Number(arrPrincipleId[i])
		payload.push({
			id: principleId,
			name: principles[principleId-1]
		})
	}
	return {
		type: 1,
		des: des,
		payload: payload,
	}
}